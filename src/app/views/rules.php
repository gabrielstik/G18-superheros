<div class="rules">
            <H2>JARVIS RULES</H2>
            <p class="imp_txt">The goal of the game is to reduce the life of your opponent's hero to zero to win the game.</p>
            <p> A player can create a
                <span>deck of 30 cards</span>, he has chosen beforehand in his
                <span>collection</span>. He has the opportunity to play against a random player or invite a friend.
                <br/> In the first round, players receive
                <span>8 cards</span>, drawn randomly from the deck. They can modify some of them to avoid having big cards impossible
                to play at the beginning of the game.
                <br/> He can place 5 visible cards and 3 hidden cards on the board. Once the card is played, it must be placed in
                the graveyard.</p>
            <p>The player starts the game with 5 energy points: he can use a point of energy to play a card, or to draw one.
                <br/> When you have more energy points you have to go through. At each turn, the player gains one point of energy.</p>

            <div class="pvcont">
                <ul>The strategy is based on the different powers of the characters, visible on the maps:

                    <li>
                        <span class="power">Attack :</span> inflicts damage to the opponent</li>
                    <li>
                        <span class="power">Defence :</span> resists his opponent's attacks</li>
                    <li>
                        <span class="power">Speed :</span> allows you to attack the opponent more quickly (the player with the highest value of the
                        cards in the highest cards attacks first) </li>
                    <li>
                        <span class="power">Intelligence :</span> makes it easier to resist attacks (the player's intelligence must be superior to
                        that of the opponent)</li>
                </ul>

                <div class="imgcard">
                    <img src="../assets/images/rarecard.png" alt="card" class="card" />
                </div>

            </div>

            <p>Each card fights the opponent's card, located in front of the game board. The life bar is used to determine the winner:
                when it reaches 0, the opposing player wins. The hero is connected to the life bar: he is hit by attacks when
                the space occupied by a map is empty, and can reach it directly. </p>

            <p>
                <span class="imp_txt">There are several aspects that can increase your chances of victory :</span>
            </p>
            <ul>
                <li> The scarcity of cards :
                    <br/> Each card has a rarity. The rarer a card is, the more power it has. It is possible to create one of these
                    cards thanks to the vibranium (see Shop).

                    <ul>Rarity of cards from the rarest to the least rare :

                        <li>Common (Gray)</li>
                        <li>Rare (Blue)</li>
                        <li>Epic (Purple)</li>
                        <li>Legendary (Orange)</li>
                    </ul>
                </li>

                <li>Affinities :
                    <br/> Each character has affinities and antagonisms.
                    <br/> If the card is combined with an ally, a bonus applies (bonus to decide).
                    <br/> If combined with an enemy, a penalty applies (malus to decide).</li>

                <li>Secret cards :
                    <br/> These are the 3 hidden cards, they correspond to bonuses or penalties affecting the values of the powers,
                    according to the character. </li>

            </ul>

            <p class="imp_txt">Grades and ranks :</p>
            <p>At the end of each game, players earn adamantium, which is the experience. Depending on his rank, a player who fights
                a player with less adamantium will get a low amount of adamantium and vice versa.
            </p>
            <p>A ranking is organized according to the rank of a player, who can evolve according to the amount of adamantium he
                has.
                <br/> When a new player registers, he belongs to the rank Wood.
            <ul>
                He can reach a higher rank by paying with adamantium :
                <li>Bronze rank costs 25,000 adamantium </li>
                <li>Silver rank costs 50,000 adamantium </li>
                <li>Gold rank costs 80,000 adamantium </li>
                <li>Platinum rank costs 100,000 adamantium </li>
                <li>Diamond rank costs 180,000 adamantium</li>
            </ul>
            </p>

